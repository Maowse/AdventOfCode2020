const fs = require("fs");
const readline = require('readline');

const readInterface = readline.createInterface({
    input: fs.createReadStream('input.txt')
});


let jolts = [];

readInterface.on('line', function(line) {
    jolts.push(parseInt(line));
});

let highestJolt = -1;
let combinations = [1];

readInterface.on('close', function() {
  jolts.sort((a,b) => a > b ? 1 : -1);
  highestJolt = jolts[jolts.length - 1] + 3;
  jolts.push(highestJolt);

  for(let i = 0; i < jolts.length; i++){
    combinations[jolts[i]] = 
     (combinations[jolts[i] - 1] || 0) 
    + (combinations[jolts[i] - 2] || 0) 
    + (combinations[jolts[i] - 3] || 0);
  }

  console.log(combinations.pop());
  
});
